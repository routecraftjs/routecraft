// Generates the per-route SEO shims that Markdoc `.md` pages can't provide
// themselves: a Markdoc page can't export `metadata`, and App Router metadata
// is per-segment, so a shared parent layout can't set per-page titles. Two kinds
// of shim are emitted, one file per route:
//
//   - docs: a `layout.tsx` next to every `src/app/docs/**/page.md` that calls
//     `docMetadata(route)`.
//   - blog: a `layout.tsx` (`blogPostMetadata` + JSON-LD) and an
//     `opengraph-image.tsx` for every post folder under `src/app/blog/*`.
//
// All of these are pure slug/route-parameterised boilerplate, so they are
// generated rather than hand-written. This runs in `prebuild` and the output is
// gitignored (see .gitignore), so the shims always match the route tree and
// nothing is committed or drifts. To regenerate by hand during local dev:
//   node --experimental-strip-types scripts/generate-route-shims.mjs
import fs from 'fs'
import path from 'path'

const appDir = path.join(process.cwd(), 'src', 'app')
const docsDir = path.join(appDir, 'docs')
const blogDir = path.join(appDir, 'blog')

const banner =
  '// Generated by scripts/generate-route-shims.mjs — do not edit by hand.'

function docLayoutSource(route) {
  return `${banner}
import { docMetadata } from '@/lib/doc-metadata'

const route = '${route}'

export const metadata = docMetadata(route)

export default function DocLayout({ children }: { children: React.ReactNode }) {
  return children
}
`
}

function blogLayoutSource(slug) {
  return `${banner}
import { blogPostMetadata, BlogPostJsonLd } from '@/lib/blog-metadata'

const slug = '${slug}'

export const metadata = blogPostMetadata(slug)

export default function PostLayout({
  children,
}: {
  children: React.ReactNode
}) {
  return (
    <>
      {children}
      <BlogPostJsonLd slug={slug} />
    </>
  )
}
`
}

function blogOgImageSource(slug, alt) {
  return `${banner}
import {
  createBlogOgImage,
  OG_CONTENT_TYPE,
  OG_SIZE,
} from '@/lib/blog-og-image'

export const size = OG_SIZE
export const contentType = OG_CONTENT_TYPE
export const alt = ${JSON.stringify(alt)}
export const dynamic = 'force-static'

export default async function Image() {
  return createBlogOgImage({ slug: '${slug}' })
}
`
}

// Frontmatter title, used as the og:image alt text (it describes the card).
function frontmatterTitle(md) {
  const block = md.match(/^---\n([\s\S]*?)\n---/)
  if (!block) return undefined
  const title = block[1].match(/^title:\s*(.+)$/m)
  return title ? title[1].trim().replace(/^["']|["']$/g, '') : undefined
}

let count = 0

// Docs: a layout.tsx beside every page.md, at any depth.
function walkDocs(dir) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name)
    if (entry.isDirectory()) {
      walkDocs(full)
    } else if (entry.name === 'page.md') {
      const route = path.relative(docsDir, dir).split(path.sep).join('/')
      fs.writeFileSync(path.join(dir, 'layout.tsx'), docLayoutSource(route))
      count++
    }
  }
}

// Blog: layout.tsx + opengraph-image.tsx for each post folder (a direct child of
// /blog containing a page.md). The index lives at /blog/page.tsx with no page.md
// and is skipped.
function walkBlog(dir) {
  if (!fs.existsSync(dir)) return
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (!entry.isDirectory()) continue
    const postDir = path.join(dir, entry.name)
    const pagePath = path.join(postDir, 'page.md')
    if (!fs.existsSync(pagePath)) continue
    const slug = entry.name
    const alt = frontmatterTitle(fs.readFileSync(pagePath, 'utf8')) ?? slug
    fs.writeFileSync(path.join(postDir, 'layout.tsx'), blogLayoutSource(slug))
    fs.writeFileSync(
      path.join(postDir, 'opengraph-image.tsx'),
      blogOgImageSource(slug, alt),
    )
    count += 2
  }
}

walkDocs(docsDir)
walkBlog(blogDir)
console.log(`Generated ${count} route shim(s).`)
