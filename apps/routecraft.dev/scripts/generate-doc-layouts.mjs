// Generates a thin `layout.tsx` next to every docs `page.md` so each doc page
// gets per-page SEO metadata (title/description/canonical/OG). Markdoc `.md`
// pages can't export metadata, and a layout is the only per-route hook.
//
// The layout only carries the route string; `docMetadata()` reads the live
// frontmatter and lead at build time, so descriptions never go stale. Re-run
// this only when doc folders are added or removed:
//   node --experimental-strip-types scripts/generate-doc-layouts.mjs
import fs from 'fs'
import path from 'path'

const docsDir = path.join(process.cwd(), 'src', 'app', 'docs')

function layoutSource(route) {
  return `// Generated by scripts/generate-doc-layouts.mjs — do not edit by hand.
import { docMetadata } from '@/lib/doc-metadata'

const route = '${route}'

export const metadata = docMetadata(route)

export default function DocLayout({ children }: { children: React.ReactNode }) {
  return children
}
`
}

let count = 0
function walk(dir) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name)
    if (entry.isDirectory()) {
      walk(full)
    } else if (entry.name === 'page.md') {
      const route = path.relative(docsDir, dir).split(path.sep).join('/')
      fs.writeFileSync(path.join(dir, 'layout.tsx'), layoutSource(route))
      count++
    }
  }
}

walk(docsDir)
console.log(`Generated ${count} doc layout(s).`)
